metrics = {}

def parse_section(platform, dimension, category, data_block):
    """Parse a metrics block"""
    if platform not in metrics:
        metrics[platform] = {}
    if dimension not in metrics[platform]:
        metrics[platform][dimension] = {}
    
    metrics[platform][dimension][category] = data_block

